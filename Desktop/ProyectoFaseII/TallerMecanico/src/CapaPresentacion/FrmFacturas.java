    public void cargarTable(){
        
    Object[] fila = new Object[6];
       float total = 0;
           float descuento = 0;
             for (EntidadDetalleFacturas det : lista) {
                 fila[0] = det.getIdProducto();
                 fila[1] = det.getNombre();
                 fila[2] = det.getPrecioVenta();
                 fila[3] = det.getCantidad();
                 fila[4] = det.getDescuento();
                 fila[5] = det.getTotal();
                 total += det.getTotal();
                 descuento += det.getDescuento();
                 modelo.addRow(fila);
             }
             txtTotal.setText(String.valueOf(total));
             txtSubTotal.setText(String.valueOf(total - descuento));
    }
    
