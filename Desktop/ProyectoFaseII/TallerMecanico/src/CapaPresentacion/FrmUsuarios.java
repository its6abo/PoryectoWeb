            private EntidadUsuarios GenerarEntidad() throws NoSuchAlgorithmException{
        EntidadUsuarios usuarios = new EntidadUsuarios();
        if (!txtId.getText().equals("")) {
            usuarios.setExiste(true);
            usuarios.setId(Integer.parseInt(txtId.getText()));
        }
                //mpresa.setId(Integer.parseInt(txtId.getText()));
                usuarios.setNombre(txtNombre.getText());
                usuarios.setCedula(txtCedula.getText());
                usuarios.setEstado(Boolean.valueOf(txtEstado.getText()));
              //usuarios.setEstado(Boolean.valueOf(txtEstado.getText()));
                usuarios.setDireccion(txtDireccion.getText());
                usuarios.setTelefono1(txtTelefono1.getText());
                usuarios.setTelefono2(txtTelefono2.getText());
                usuarios.setCorreo(txtCorreo.getText());
                usuarios.setPass(new BLUsuarios().toHexString("12345"));
                if(cbmPerfiles.getSelectedItem().toString().equals("Administrador")){
                usuarios.setIdPerfil(1);
           
                
                }
                
                else if(cbmPerfiles.getSelectedItem().toString().equals("Cajero")){
                usuarios.setIdPerfil(2);
           
                
                }
                
                else if(cbmPerfiles.getSelectedItem().toString().equals("Empleado")){
                usuarios.setIdPerfil(3);
           
                
                }
                   
                else if(cbmPerfiles.getSelectedItem().toString().equals("Bodega")){
                usuarios.setIdPerfil(4);
           
                
                }
                

        return usuarios;
         }
    
    
    
    
    
