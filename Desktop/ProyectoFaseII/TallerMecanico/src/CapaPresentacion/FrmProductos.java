    private void tblProductosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblProductosMouseClicked
        // TODO add your handling code here:
        //AL DARLE DOBLE CLIC A LA TABLA CARGAR LOS DATOS DE UN DETERMINADO CLIENTE

        BLProductos logica = new BLProductos();
        EntidadProductos producto;
        String condicion;
        try {
            if (evt.getClickCount() == 2) {
                int fila = tblProductos.rowAtPoint(evt.getPoint());
                txtId.setText(tblProductos.getValueAt(fila, 0).toString());
                txtIdEmpresa.setText(tblProductos.getValueAt(fila, 1).toString());
                txtDescripcion.setText(tblProductos.getValueAt(fila, 2).toString());
                txtPrecioCosto.setText(tblProductos.getValueAt(fila, 3).toString());
                txtPrecioVenta.setText(tblProductos.getValueAt(fila, 4).toString());
                txtEstado.setText(tblProductos.getValueAt(fila, 5).toString());
                
                


                condicion = String.format("estado = 1 and id=%s", txtId.getText());
                producto = logica.ObtenerUnProducto(condicion);
                txtId.setText(String.valueOf(producto.getId()));
                txtIdEmpresa.setText(String.valueOf(producto.getIdEmpresa()));
                txtDescripcion.setText(producto.getDescripcion());
                txtPrecioCosto.setText(String.valueOf(producto.getPrecioCosto()));
                txtPrecioVenta.setText(String.valueOf(producto.getPrecioVenta()));
                txtEstado.setText(String.valueOf(producto.getEstado()));

            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_tblProductosMouseClicked


    
    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:

        FrmBuscarProductos buscar = new FrmBuscarProductos(null, true);
        buscar.addWindowListener(new WindowAdapter() {
            @Override
            public void windowClosed(WindowEvent e) {
                try {
                    int id = buscar.ObtenerId();
                    String condicion = "";
                    BLProductos logica = new BLProductos();
                    EntidadProductos producto;
                    if (id > -1) {
                        condicion = String.format("estado = 1 and Id=%d", id);
                        producto = logica.ObtenerUnProducto(condicion);
                        txtId.setText(String.valueOf(producto.getId()));
                        txtIdEmpresa.setText(String.valueOf(producto.getIdEmpresa()));
                        txtDescripcion.setText(producto.getDescripcion());
                        txtPrecioCosto.setText(String.valueOf(producto.getPrecioCosto()));
                        txtPrecioVenta.setText(String.valueOf(producto.getPrecioVenta()));
                        txtEstado.setText(String.valueOf(producto.getEstado()));
                    }
                    else{
                        txtId.setText(null);
                        txtIdEmpresa.setText(null);
                        txtDescripcion.setText(null);
                        txtPrecioCosto.setText(null);
                        txtPrecioVenta.setText(null);
                        txtPrecioCosto.setText(null);
                        txtEstado.setText(null);
                    }
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(null, ex.getMessage());
                }
            }
        });
        buscar.setVisible(true);

    }//GEN-LAST:event_btnBuscarActionPerformed

