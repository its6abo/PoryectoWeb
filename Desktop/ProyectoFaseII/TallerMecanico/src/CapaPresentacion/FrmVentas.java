    private void btnBuscarUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarUsuarioActionPerformed
        // BUSCA EL CLIENTE

        FrmBuscarClientes buscar = new FrmBuscarClientes(null, true);
        buscar.addWindowListener(new WindowAdapter() {
            @Override
            public void windowClosed(WindowEvent e) {
                try {
                    int id = buscar.ObtenerId();
                    String condicion = "";
                    BLUsuarios logica = new BLUsuarios();
                    EntidadUsuarios usuarios;
                    if (id > -1) {
                        condicion = String.format("estado = 1 and Id=%d", id);
                        usuarios = logica.ObtenerUnUsuarios(condicion);
                        txtIdUsuario.setText(String.valueOf(usuarios.getId()));
                        txtNombreUsuario.setText(usuarios.getNombre());
                    }
                    else{
                        txtIdUsuario.setText(null);
                        txtNombreUsuario.setText(null);
                    }
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(null, ex.getMessage());
                }
            }
        });
        buscar.setVisible(true);

    }//GEN-LAST:event_btnBuscarUsuarioActionPerformed

