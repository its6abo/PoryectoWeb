    private void LimpiarTabla(){
        modelo = new DefaultTableModel(){
            @Override
            public boolean isCellEditable(int row, int column){
                return false;
            }
        };     
        tblProductos.setModel(modelo);
        modelo.addColumn("Id");
        modelo.addColumn("IdEmpresa");
        modelo.addColumn("descripcion");
        modelo.addColumn("precioCosto");
        modelo.addColumn("precioVenta");
        modelo.addColumn("estado");
        modelo.addColumn("stock");
    }
    
    //MÉTODO PARA MOSTRAR LA TABLA
    private void CargarDatos(String condicion) throws Exception {
        BLProductos logica = new BLProductos();
        List<EntidadProductos> lista;
        LimpiarTabla();
        Object[] fila = new Object[8];
        try {
            lista = logica.ListarProductos(condicion);
            for (EntidadProductos cli : lista) {
                fila[0] = cli.getId();
                fila[1] = cli.getIdEmpresa();
                fila[2] = cli.getDescripcion();
                fila[3] = cli.getPrecioCosto();
                fila[4] = cli.getPrecioVenta();
                fila[5] = cli.getEstado();
                fila[6] = cli.getStock();
                fila[7] = cli.getFecha();
                
                modelo.addRow(fila);
            }
        } catch (Exception ex) {
             JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }

       
    
            private EntidadProductos GenerarEntidad(){
        EntidadProductos producto = new EntidadProductos();
        if (!txtId.getText().equals("")) {
            producto.setExiste(true);
            producto.setId(Integer.parseInt(txtId.getText()));
        }
                //producto.setIdEmpresa(Integer.parseInt(txtId.getText()));
                producto.setIdEmpresa(Integer.parseInt(txtIdEmpresa.getText()));
                producto.setDescripcion(txtDescripcion.getText());
                producto.setPrecioCosto(Integer.parseInt(txtPrecioCosto.getText()));
                producto.setPrecioVenta(Integer.parseInt(txtPrecioVenta.getText()));
        return producto;
         }
    
    
    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:

        BLProductos logica = new BLProductos();
        EntidadProductos producto;
        try {
            producto = GenerarEntidad();
            if(producto.getExiste()){
                if(logica.Eliminar(producto) > 0){
                    JOptionPane.showMessageDialog(this, logica.getMensaje());
                    Limpiar();
                    CargarDatos("estado = 1");
                }else{
                    JOptionPane.showMessageDialog(this, "No fue posible eliminar el prodcuto");
                }
            }else{
                JOptionPane.showMessageDialog(this, "Debe seleccionar el producto que desea eliminar");
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

