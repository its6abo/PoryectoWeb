    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:

        BLUsuarios logica = new BLUsuarios();
        EntidadUsuarios usuarios;
        try {
            usuarios = GenerarEntidad();
            if(usuarios.getExiste()){
                if(logica.Eliminar(usuarios) > 0){
                    JOptionPane.showMessageDialog(this, logica.getMensaje());
                   Limpiar();
                    CargarDatos("estado = 1");
                }else{
                    JOptionPane.showMessageDialog(this, "No fue posible eliminar el usuario");
                }
            }else{
                JOptionPane.showMessageDialog(this, "Debe seleccionar el usuario que desea eliminar");
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

