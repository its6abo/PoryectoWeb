    private void CargarDatos(String condicion) throws Exception {
        BLUsuarios logica = new BLUsuarios();
        List<EntidadUsuarios> lista;
        LimpiarTabla();
        Object[] fila = new Object[10];
        try {
            lista = logica.ListarUsuarios(condicion);
            for (EntidadUsuarios cli : lista) {
                fila[0] = cli.getId();
                fila[1] = cli.getNombre();
                fila[2] = cli.getCedula();
                fila[3] = cli.isEstado();
                fila[4] = cli.getDireccion();
                fila[5] = cli.getTelefono1();
                fila[6] = cli.getTelefono2();
                fila[7] = cli.getCorreo();
                fila[8] = cli.getFecha();
                fila[9] = cli.getIdPerfil();
                
                modelo.addRow(fila);
            }
        } catch (Exception ex) {
             JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }
    
    
    
    
    
