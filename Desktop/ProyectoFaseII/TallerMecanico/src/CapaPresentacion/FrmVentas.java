     private void LimpiarTabla(){
        modelo = new DefaultTableModel(){
            @Override
            public boolean isCellEditable(int row, int column){
                return false;
            }
        };     
        tblVentas.setModel(modelo);
        modelo.addColumn("Id Factura");
        modelo.addColumn("Total");
        modelo.addColumn("Descuento");
        modelo.addColumn("Fecha");
        modelo.addColumn("Cliente");
        modelo.addColumn("Usuario");

         }
    
    private void btnMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostrarActionPerformed
    try{
        
        if(!txtIdUsuario.getText().equals("")){
         
        String desde = jDesde.getFormattedTextField().getText();
        String hasta = jHasta.getFormattedTextField().getText();
        BLFacturaClientes logica = new BLFacturaClientes();           
            LimpiarTabla();
            Object[] fila = new Object[9];
             List<EntidadFacturaClientes> lista;
             String condicion = " CONVERT(DATE,f.fecha,121) BETWEEN CONVERT(DATE,'"+ desde +"',121) AND CONVERT(DATE,'"+ hasta +"',121) AND idUsuario = " + txtIdUsuario.getText();
        try {
            lista = logica.Listar(condicion);
            for (EntidadFacturaClientes cli : lista) {
                fila[0] = cli.getId();
                fila[1] = cli.getTotal();
                fila[2] = cli.getDescuento();
                fila[3] = cli.getFecha();
                fila[4] = cli.getCliente();
                fila[5] = cli.getUsuario();                
                modelo.addRow(fila);
            }
        } catch (Exception ex) {
             JOptionPane.showMessageDialog(this, ex.getMessage());
        }
         
     }else
          JOptionPane.showMessageDialog(null, "Debe de seleccionar un usuario");
    }catch(Exception ex){
        
    }
    }//GEN-LAST:event_btnMostrarActionPerformed

