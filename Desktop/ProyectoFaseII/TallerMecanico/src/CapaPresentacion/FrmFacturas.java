    private void tblFacturaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblFacturaMouseClicked
    if (evt.getClickCount() == 2)
    {
        if (JOptionPane.showConfirmDialog(rootPane, "Está seguro que desea eliminar este producto, ¿desea continuar?",
        "Eliminar Registro", JOptionPane.WARNING_MESSAGE, JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION)
        {
            if(lista != null){                
                int fila = tblFactura.rowAtPoint(evt.getPoint());
                int idProd = 0;
                ArrayList<EntidadDetalleFacturas> lista2 = lista;
                for (EntidadDetalleFacturas det : lista2)
                {
                    idProd = Integer.parseInt(tblFactura.getValueAt(fila, 0).toString());
                    if (det.getIdProducto() == idProd)
                    {
                     lista.remove(det);
                      break;
                    }
                 }
              LimpiarTabla();
              cargarTable();
            } 
        }
    }        
        
    }//GEN-LAST:event_tblFacturaMouseClicked

