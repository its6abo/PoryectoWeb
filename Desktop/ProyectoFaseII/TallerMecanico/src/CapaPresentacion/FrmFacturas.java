    private EntidadFacturaClientes GenerarEntidad(){
        EntidadFacturaClientes factura = new EntidadFacturaClientes();

                //mpresa.setId(Integer.parseInt(txtId.getText()));
                factura.setIdEmpresa(1);
                factura.setIdCliente(Integer.parseInt(txtIdCliente.getText()));
                factura.setIdUsuario(FrmMenu.miUsuario.getId());
              //usuarios.setEstado(Boolean.valueOf(txtEstado.getText()));
                factura.setDescuento(0);
                factura.setTotal(Float.parseFloat(txtTotal.getText()));
                factura.setListaDetalles(lista);
                
                if(cbmTipoFactura.getSelectedItem().toString().equals("Factura Normal")){
                factura.setIdTipoFactura(1);  
                }                
                else if(cbmTipoFactura.getSelectedItem().toString().equals("Orden De Reparación")){
                factura.setIdTipoFactura(2);
                }                
                else if(cbmTipoFactura.getSelectedItem().toString().equals("Proforma")){
                factura.setIdTipoFactura(3);
                }
                   
        return factura;
         }
   
